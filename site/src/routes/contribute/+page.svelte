<h1>Contributing to The Autonomy Protocol</h1>

<p>Thank you for your interest in contributing to The Autonomy Protocol. To maintain the pedagogical integrity and professional standards required for public school implementation, we ask that all contributors adhere to the following guidelines.</p>

<h2>Philosophical Alignment</h2>

<h3>The Autonomy Protocol is built on the principle of Internal Self-Governance. All contributions must avoid:</h3>

<ul>
<li><strong>Political Bias:</strong> Content must remain neutral and focused on universal cognitive skills rather than specific social or political agendas.</li>
<li><strong>Secular Rigor:</strong> Ensure all frameworks are stripped of religious or therapeutic terminology, focusing instead on observable behavioral executive functions.</li>
<li><strong>Subjectivity:</strong> Prioritize data-driven, observable learning outcomes over subjective emotional states.</li>
</ul>

<h2>Content Standards</h2>

<p>When contributing lesson plans, practicums, or vocational paths, utilize the following structural standards:</p>

<h3>1. The Approved Lexicon</h3>

<p>Always utilize the "Human Operating System" terminology established in the GEMINI.md file.</p>

<ul>
<li><strong>Refer to accountability as:</strong> Systems Maintenance or Internal Regulation.</li>
<li><strong>Refer to interpersonal repair as:</strong> The Amends Protocol or Restorative Practice.</li>
<li><strong>Refer to self-correction as:</strong> Real-Time Debugging or Cognitive Inventory.</li>
</ul>

<h3>2. Formatting & Aesthetic</h3>

<p>To maintain consistency across our digital and physical assets, follow these formatting rules:</p>

<ul>
<li><strong>Dark-Theme Optimization: Use clean Markdown with high-contrast hierarchies (Headings, Tables, Blockquotes).</li>
<li><strong>Academic Tone: Maintain a collegiate, professional, and objective voice.</li>
<li><strong>Visual Clarity: Use bulleted lists and tables to ensure information is scannable for busy administrators and educators.</li>
</ul>

<h2>How to Contribute</h2>

Step 1: Claim or Open an Issue

Before starting work, browse the current Kanban Boards or Issues. If you have a new idea for a vocational path (e.g., Law, Arts, or Agriculture), open a new Issue for "System Architecture Review" first.

Step 2: Drafting Modules

When drafting a new module, use the LESSON_PLAN_TEMPLATE.md. Every contribution must include:

Objective: What specific behavioral shift is being addressed?
Pedagogical Logic: Why does this specific exercise result in that shift?
The Practicum: A concrete, applied learning exercise for students.
Assessment: How will an educator measure success?
Step 3: Peer Review

Submit your contribution via a Pull Request. Every module must be reviewed by at least one peer to ensure it meets our standards for:

Neutrality: Is it free of partisan or sectarian language?
Clarity: Is the exercise easy for a high school freshman to execute?
Utility: Does it solve a real-world problem for a graduating senior?
🚦 Community Code of Conduct

We practice what we teach. All contributors are expected to operate under the Integrity Protocol:

Be Impeccable With Your Word: Follow through on your commits and maintain high standards of accuracy.
Don't Take Feedback Personally: Reviews are intended to optimize the system, not critique the individual.
Don't Make Assumptions: If a task or standard is unclear, ask for clarification immediately.
Always Do Your Best: Contribute high-quality work that honors the project's mission.
Thank you for helping us build the infrastructure for a self-governed future.

