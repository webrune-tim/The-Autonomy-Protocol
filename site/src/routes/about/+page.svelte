				<li>
					<strong>Daily Check-In:</strong> Spend a few minutes every day making sure you're
					still on track.
				</li>
			</ul>
		</li>
		<li>
			<h3>The Integrity Protocol (Your Social Shield)</h3>
			<p>
				These are the 4 rules for dealing with the world without getting sucked into
				the drama.
			</p>

			<ul>
				<li>
					<strong>Watch Your Words:</strong> Say what you mean and keep your promises.
				</li>
				<li>
					<strong>Don't Take It Personally:</strong> What other people say about you is
					usually about them, not you.
				</li>
				<li>
					<strong>Stop Guessing:</strong> Don't assume you know what someone else is thinking.
					Ask questions instead.
				</li>
				<li>
					<strong>Just Do Your Best:</strong> Your "best" changes every day. Just give
					what you've got for that moment.
				</li>
			</ul>
		</li>
